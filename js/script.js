let activeTab = "all";

function jbs() {
  const container = document.getElementById("jobContainer");
  // console.log(container);
  const emptyMessage = document.getElementById("emptyMessage");
  // console.log(emptyMessage);

  container.innerHTML = "";

  const filtered = jobs.filter((job) => {
    if (activeTab === "all") return true;
    return job.status === activeTab;
  });

  if (filtered.length === 0) {
    emptyMessage.classList.remove("hidden");
  } else {
    emptyMessage.classList.add("hidden");
  }

  document.getElementById("tabCount").innerText = filtered.length + " jobs";

  filtered.forEach((job) => {
    let badgeText = "NOT APPLIED";
    let badgeClass = "bg-gray-200 text-blue-900";

    if (job.status === "interview") {
      badgeText = "INTERVIEW";
      badgeClass = "bg-green-100 text-green-700";
    }

    if (job.status === "rejected") {
      badgeText = "REJECTED";
      badgeClass = "bg-red-100 text-red-700";
    }

    const card = document.createElement("div");
    card.className = "bg-gray-50 p-6 rounded-lg border";

    let interviewBtnClass =
      "px-4 py-2 border rounded text-green-600 border-green-500";

    if (job.status === "interview") {
      interviewBtnClass = "px-4 py-2 border rounded bg-green-500 text-white";
    }

    let rejectedBtnClass =
      "px-4 py-2 border rounded text-red-600 border-red-500";

    if (job.status === "rejected") {
      rejectedBtnClass = "px-4 py-2 border rounded bg-red-500 text-white";
    }

    card.innerHTML = `
      <div class="flex justify-between">
        <div>
          <h3 class="font-bold text-blue-900">${job.company}</h3>
          <p class="text-gray-600">${job.position}</p>
          <p class="text-sm text-gray-500">${job.location} , ${job.type} , ${job.salary}</p>

          <span class="inline-block mt-3 px-3 py-1 rounded ${badgeClass}">
            ${badgeText}
          </span>

          <p class="mt-3 text-gray-700">${job.description}</p>
        </div>

        <button onclick="removes(${job.id})">
          <i class="fa-regular fa-trash-can"></i>
        </button>
      </div>

<div class="mt-4 flex gap-3">
  <button onclick="status(${job.id}, 'interview')" 
    class="${interviewBtnClass}">
    Interview
  </button>

  <button onclick="status(${job.id}, 'rejected')" 
    class="${rejectedBtnClass}">
    Rejected
  </button>
</div>
    `;

    container.appendChild(card);
  });

  changes();
}

function status(id, newStatus) {
  let job = jobs.find(function (item) {
    return item.id === id;
  });

  if (job) {
    job.status = newStatus;
    jbs();
  } else {
    console.log("Job not found");
  }
}

function removes(id) {
  let index = jobs.findIndex(function (item) {
    return item.id === id;
  });

  if (index !== -1) {
    jobs.splice(index, 1);
    jbs();
  } else {
    console.log("Job not found");
  }
}

function changes() {
  document.getElementById("totalCount").innerText = jobs.length;

  const interview = jobs.filter((j) => j.status === "interview").length;
  // console.log(interview);
  const rejected = jobs.filter((j) => j.status === "rejected").length;
  // console.log(rejected);

  document.getElementById("interviewCount").innerText = interview;
  document.getElementById("rejectedCount").innerText = rejected;
}

document.querySelectorAll(".tabBtn").forEach((btn) => {
  btn.addEventListener("click", function () {
    document.querySelectorAll(".tabBtn").forEach((b) => {
      b.classList.remove("bg-blue-500", "text-white");
      b.classList.add("bg-gray-200");
    });

    this.classList.remove("bg-gray-200");
    this.classList.add("bg-blue-500", "text-white");

    activeTab = this.dataset.tab;

    jbs();
  });
});

jbs();
